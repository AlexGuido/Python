'''
Diseña un programa que pida el valor de la base y la altura de un triangulo y muestre el valor de su área. (
Prueba que tu programa funciona correctamente con este ejemplo: si la base es 10  y la altura 100, el área
será 500.0)
'''
base=float(input("ingresa el valor de la base del triangulo: "))
altura= float(input("Ingresa el valor de la altura del triangulo: "))

area= (base*altura)/2
print("El área es: ",area)
